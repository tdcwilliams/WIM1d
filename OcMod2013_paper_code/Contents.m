% The chebfun system extends familiar powerful methods of numerical
% computation involving numbers to continuous or piecewise-continuous
% functions.  It also implements continuous analogues of linear algebra
% notions like the QR decomposition and the SVD.  The mathematical basis of
% the system combines tools of Chebyshev expansions, fast Fourier
% transform, barycentric interpolation, Clenshaw-Curtis quadrature, and
% recursive zerofinding.
%
% To get started on the chebfun system, see the user guides in the Help
% brower window or in the guide/html subdirectory of the chebfun
% installation directory. 
