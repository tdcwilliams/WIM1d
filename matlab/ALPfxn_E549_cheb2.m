function [alp,Tsq]   = ALPfxn_E549_cheb2(om,h);
%% CALL: [alp,Tsq]   = ALPfxn_E549_cheb2(om,h);
%% alp is the serial approximation for the attenuation coefficient (energy)
%%  using E=5.49GPa (Vernon's E with vbf=[brine vol frac]=.1);
%% Tsq=|T^2|; may be necessary for W;

if nargin==0
   h  = 1
   T  = 10
   om = 2*pi/T;
end


if h==0
   alp   = 0*om;
   Tsq   = 1;
   return;
end

TAKE_LOGS   = 0;%% TAKE_LOGS = 1, interpolate log(alpha) (gives alpha>0 always)
                %% TAKE_LOGS = 0, do alpha itself (less error);
Ncheb_f     = 26;%% deg of poly in period/freq; 
Ncheb_h     = 26;%% deg of poly in thickness;

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% INPUT AND OUTPUT FILES:

%% copy cheb coeff's from
%% 'datfiles/cheb_alp_E549.dat'
%% into function 'alp_coeffs_fn'
%% at the bottom of this file;
%% copy cheb coeff's from
%% 'datfiles/cheb_Tsq_E549.dat'
%% into function 'Tsq_coeffs_fn'
%% at the bottom of this file;
%%
%% Files generated with MF_RTSET_si_sheet_SUB_Gal_youngs.m


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% SET INPUT TYPES:

%% X_INPUT  = 1 for period;
%% X_INPUT  = 2 for freq;
%% X_INPUT  = 3 for omega;
X_INPUT  = 3;

%% AC_INPUT  = 1 if input AC is energy AC;
%% AC_INPUT  = 2 if input AC is amplitude AC;
AC_INPUT = 1;

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% RANGE OF VALIDITY VARIES FROM RUN TO RUN,
%%  SO NEED TO CHECK THIS EACH TIME;
hmax  = 8.25;
hmin  = .25;
xmin  = 2*pi/35;%% Tmax=35s
xmax  = 2.692793703076966;%% Tmin=2.33333333333s
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% EVERYTHING FROM HERE ON SHOULD BE THE SAME
%%  FOR EACH RUN;
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

ZZ                = min(h/hmin,1);
h(find(ZZ<1))     = hmin;
h(find(h>hmax))   = hmax;
%jout  = find(h>hmax | h<hmin);
%if ~isempty(jout)
%   disp(sprintf('h outside range [%d,%d]',hmin,hmax));
%   disp(sprintf('h = %d m',h(jout)));
%   return;
%end
%%

if X_INPUT==1
   x     = (2*pi)./om;
   xstr  = 'period';
elseif X_INPUT==2
   x     = om/(2*pi);
   xstr  = 'freq';
else
   x     = om;
   xstr  = 'omega';
end

jout  = find(x>xmax | x<xmin);
if ~isempty(jout)
   x(jout)
   disp([xstr,...
     sprintf(' outside range [%d,%d]',xmin,xmax)]);
   return;
end

alp_coeffs  = alp_coeffs_fn(Ncheb_f,Ncheb_h); 
Tsq_coeffs  = Tsq_coeffs_fn(Ncheb_f,Ncheb_h); 
Limits      = [xmin,xmax,hmin,hmax];

%% DO INTERPOLATION:
y_coeffs    = alp_coeffs+1i*Tsq_coeffs;
y           = OP_chebinterp2d(x,h,y_coeffs,Limits);

%x,h,y_coeffs(1:20),Limits,pause
%y_coeffs(:,2),pause

%% want to OUTPUT ENERGY attenuation coefficients:
if TAKE_LOGS==0
   alp   = max(0,AC_INPUT*real(y));%%alp>0
   Tsq   = max(0,AC_INPUT*imag(y));%%|T|^2>0
else
   alp   = AC_INPUT*exp(real(y));
   Tsq   = AC_INPUT*exp(imag(y));
end

%% fix for h outside the range;
alp   = alp*diag(ZZ);
Tsq   = 1+(Tsq-1)*diag(ZZ);

